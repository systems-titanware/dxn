const SETUP_LOCK_FILENAME: &str = ".dxn-setup-lock.json";

fn ensure_provisioned_files(config_path: &str) -> io::Result<()> {
    let config = Path::new(config_path);
    if !config.is_file() {
        return Err(io::Error::new(
            io::ErrorKind::NotFound,
            "config.json not found. This file must be generated by dxn-setup.",
        ));
    }

    let lock_path = config
        .parent()
        .unwrap_or_else(|| Path::new("."))
        .join(SETUP_LOCK_FILENAME);
    if !lock_path.is_file() {
        return Err(io::Error::new(
            io::ErrorKind::NotFound,
            "DXN core is not provisioned. Run dxn-setup before starting the server.",
        ));
    }

    Ok(())
}

