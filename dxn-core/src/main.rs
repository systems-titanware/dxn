#[actix_web::main]
async fn main() -> std::io::Result<()> {

    logger::log("App initialized");
    // DATA
    let file_path = "./config.json".to_string();
    ensure_provisioned_files(&file_path)?;
    
    let system_data = system::serialization::json::deserialize::<System>(file_path.clone());
    
    let project_root = system::files::project_root::resolve_project_root_with_config(&file_path)
        .map_err(|e| std::io::Error::new(std::io::ErrorKind::Other, e))?;
    let auth_root = system::files::project_root::resolve_auth_root_with_config(&file_path, &project_root)
        .map_err(|e| std::io::Error::new(std::io::ErrorKind::Other, e))?;
    let dxn_files_root = system::files::project_root::dxn_files_root(&project_root);
    if !std::path::Path::new(&dxn_files_root).is_dir() {
        return Err(std::io::Error::new(
            std::io::ErrorKind::NotFound,
            format!("DXN files root not found: {}", dxn_files_root),
        ));
    }
    logger::log(format!("Resolved project_root: {}", project_root).as_str());
    logger::log(format!("Resolved dxn_files_root: {}", dxn_files_root).as_str());
    logger::log(format!("Resolved auth_root: {}", auth_root).as_str());
    system::files::manager::set_project_root(&project_root);
    let sa_identity = system::auth::sa_identity::load_sa_identity(&auth_root).ok();
    if sa_identity.is_none() {
        logger::log("SA identity file not found; auth and guarded routes will return 503/401 until .sa-identity.json is provisioned.");
    }
    let app = web::Data::new(AppState {
        app_name: String::from("dxnet"),
        counter: RwLock::new(0),
        db_name: String::from("person"),
        // TODO: Fix below to match if let, else
        system: system_data.unwrap(),
        uuid: Uuid::now_v7(),
        project_root,
        dxn_files_root,
        sa_identity,
    });


    /// DB
    // Note: config.json is now loaded above and used for system initialization
 
    // Bootstrap schemas (hybrid approach: config + runtime schemas)
    println!("Init -> Schema Repository");
    bootstrap_schemas(app.system.data.clone()); 
    
    // Bootstrap file directories (hybrid approach: config + runtime directories)
    println!("Init -> File Directories");
    bootstrap_files(app.system.files.clone(), &app.project_root);
    
    println!("Init -> Integrations");
    crate::integrations::manager::set_project_root(&app.project_root);
    create_integrations(app.system.integrations.clone()); 

    println!("Init -> Functions");
    create_functions(app.system.functions.clone());

    println!("Init -> Server");
 
    // Determine environment: default to "development" if DXN_ENV is not set
    let env = env::var("DXN_ENV").unwrap_or_else(|_| "development".to_string());
    let is_dev = env == "development";

    HttpServer::new(move || {
        // Configure CORS based on environment
        let cors = if is_dev {
            // In development, allow any origin for easier local testing (web, simulator, devices)
            Cors::default()
                .allow_any_origin()
                .allowed_methods(vec!["GET", "POST", "PUT", "DELETE", "OPTIONS"])
                .allowed_headers(vec![header::AUTHORIZATION, header::ACCEPT, header::CONTENT_TYPE])
                .max_age(3600)
        } else {
            // In non-development environments, restrict origins explicitly.
            // Adjust allowed origins as needed for your deployment.
            Cors::default()
                .allowed_methods(vec!["GET", "POST", "PUT", "DELETE", "OPTIONS"])
                .allowed_headers(vec![header::AUTHORIZATION, header::ACCEPT, header::CONTENT_TYPE])
                .supports_credentials()
                .max_age(3600)
        };

        App::new()
            .wrap(cors)
            // Configure app state
            .app_data(app.clone())
            // Auth: login only (no guard)
            .service(web::scope("/api/auth").configure(system::auth::controller::config))
            // Guarded API and dashboard: require Authorization: Bearer <token>
            .service(
                web::scope("/api/data")
                    .wrap(actix_web::middleware::from_fn(system::auth::middleware::sa_auth_middleware))
                    .configure(system::server::http::controllers::data::config),
            )
            .service(
                web::scope("/api/function")
                    .wrap(actix_web::middleware::from_fn(system::auth::middleware::sa_auth_middleware))
                    .configure(|cfg| { system::server::http::controllers::function::config(cfg, app.system.functions.clone())}),
            )
            .service(
                web::scope("/api/config")
                    .wrap(actix_web::middleware::from_fn(system::auth::middleware::sa_auth_middleware))
                    .configure(|cfg| { system::server::http::controllers::config::config(cfg)}),
            )
            .service(
                web::scope("/api/schema")
                    .wrap(actix_web::middleware::from_fn(system::auth::middleware::sa_auth_middleware))
                    .configure(|cfg| { system::server::http::controllers::schema::config(cfg)}),
            )
            .service(
                web::scope("/api/events")
                    .wrap(actix_web::middleware::from_fn(system::auth::middleware::sa_auth_middleware))
                    .configure(system::server::http::controllers::events::config),
            )
            .service(
                web::scope("/api/files")
                    .wrap(actix_web::middleware::from_fn(system::auth::middleware::sa_auth_middleware))
                    .configure(system::server::http::controllers::files::config),
            )
            .service(
                web::scope("/server")
                    .wrap(actix_web::middleware::from_fn(system::auth::middleware::sa_auth_middleware))
                    .configure(|cfg| system::server::http::controllers::server::config(cfg, app.system.server.clone())),
            )
            
            
            // Add default route
            .route("/_.stats", web::get().to(stats))
            .route("/", web::get().to(DXN))
    })
    .bind(("127.0.0.1", 8080))?
    .run()
    .await
 

}





